package com.model;

public class BedStatusObject 
{
	private String wardname;
	private String totalbed;
	private String occupiedbed;
	private String vacantbed;
	
	public String getWardname() {
		return wardname;
	}
	public void setWardname(String wardname) {
		this.wardname = wardname;
	}
	public String getTotalbed() {
		return totalbed;
	}
	public void setTotalbed(String totalbed) {
		this.totalbed = totalbed;
	}
	public String getOccupiedbed() {
		return occupiedbed;
	}
	public void setOccupiedbed(String occupiedbed) {
		this.occupiedbed = occupiedbed;
	}
	public String getVacantbed() {
		return vacantbed;
	}
	public void setVacantbed(String vacantbed) {
		this.vacantbed = vacantbed;
	}
	
	
}
